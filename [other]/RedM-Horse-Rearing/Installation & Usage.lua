# 马后仰

这是一个非常简单的脚本，允许玩家执行RDO中的技巧，即“后仰”。

# 安装

将“HorseRearing”拖到资源文件夹中，并在服务器.cfg中添加“ensure HorseRearing”。
重新启动服务器，

或者在游戏中使用F8控制台，执行命令“refresh”和“start HorseRearing”来开始游戏。
*注意，这只会在会话中启动它。将其添加到服务器.cfg以确保它能在服务器启动时自动启动。

安装完成后，你就可以使用了。

# 用法

这个脚本非常简单，有两个步骤。

上马步，然后在聊天框中输入“/rear”，观看你的马执行后仰技巧。

如果你执行命令时没有骑在马上，它会在F8控制台中告诉你你没有骑在马上。
如果它告诉你没有骑在马上，但实际上你是骑在马上的，请告诉我是哪个马角色，我会修复它。

